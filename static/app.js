function formatPageProgress(progressData) {
    const annotated = Number(progressData?.annotated_count ?? 0);
    const total = Number(progressData?.total_pages ?? 0);
    const progress = progressData?.progress ?? (total > 0 ? Math.round(annotated / total * 100) : 0);
    return `${annotated}/${total}(${Number(progress)}%)`;
}

function updatePageProgressDisplay(progressData) {
    if (!elements.pageProgress) return;
    elements.pageProgress.textContent = formatPageProgress(progressData);
}

function getDatasetProgress(books = state.books) {
    const totals = books.reduce((acc, book) => {
        acc.annotated_count += Number(book.annotated_count ?? 0);
        acc.total_pages += Number(book.total_pages ?? 0);
        return acc;
    }, { annotated_count: 0, total_pages: 0 });
    totals.progress = totals.total_pages > 0
        ? Math.round(totals.annotated_count / totals.total_pages * 100)
        : 0;
    return totals;
}

function updateDatasetProgressDisplay() {
    updatePageProgressDisplay(getDatasetProgress());
}

async function loadBooks() {
    try {
        const res = await fetch('/api/books');
        const data = await res.json();
        state.books = data.books || [];
        updateDatasetProgressDisplay();
        
        elements.bookSelect.innerHTML = '<option value="">書籍を選択...</option>';
        state.books.forEach(book => {
            const option = document.createElement('option');
            option.value = book.book_id;
            option.textContent = formatBookOptionLabel(book);
            elements.bookSelect.appendChild(option);
        });
        
        // 起動時に最初の書籍を自動で開く
        if (state.books.length > 0) {
            const firstBook = state.books[0];
            elements.bookSelect.value = firstBook.book_id;
            await loadPages(firstBook.book_id);
            await refreshModelStatus(firstBook.book_id);
            
            // アノテーション済みページがあればその最後を、なければ最初のページを開く
            if (state.pages.length > 0) {
                const targetPageId = state.lastAnnotated || state.pages[0];
                elements.pageSelect.value = targetPageId;
                await loadPageData(firstBook.book_id, targetPageId);
            }
        } else {
            updatePageProgressDisplay(null);
        }
    } catch (error) {
        console.error('書籍一覧の取得に失敗:', error);
        updatePageProgressDisplay(null);
    }
}

