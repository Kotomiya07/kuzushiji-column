function buildSegmentPredictionFeedbackFrom(sourceState) {
    const source = sourceState.predictionMeta?.segmentSource;
    const boundaries = sourceState.predictionMeta?.segmentBoundaries || [];
    if (!['segment_boundary_model', 'global_segment_boundary_model'].includes(source) || boundaries.length === 0) {
        return null;
    }

    const segmentIndexByColumnId = new Map();
    sourceState.segments.forEach((segment, segIdx) => {
        segment.forEach((colIdx) => {
            const columnId = sourceState.columnIds[colIdx];
            if (columnId) segmentIndexByColumnId.set(columnId, segIdx);
        });
    });

    let acceptedCount = 0;
    boundaries.forEach((boundary) => {
        const leftSeg = segmentIndexByColumnId.get(boundary.left_column_id);
        const rightSeg = segmentIndexByColumnId.get(boundary.right_column_id);
        const actualBreak = leftSeg !== undefined && rightSeg !== undefined && leftSeg !== rightSeg;
        if (Boolean(boundary.predicted_break) === actualBreak) {
            acceptedCount += 1;
        }
    });

    return {
        task_type: 'segment',
        source,
        accepted_count: acceptedCount,
        corrected_count: boundaries.length - acceptedCount,
        total_count: boundaries.length,
        page_id: sourceState.pageId,
    };
}

