function unicodeToChar(unicode) {
    if (!unicode) return '';
    const s = String(unicode).trim();
    const m = s.match(/^U\+([0-9A-Fa-f]{4,6})$/);
    if (!m) return '';
    const cp = Number.parseInt(m[1], 16);
    if (!Number.isFinite(cp)) return '';
    try {
        return String.fromCodePoint(cp);
    } catch {
        return '';
    }
}

