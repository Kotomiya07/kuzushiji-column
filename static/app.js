function drawCentroidLine(targetCtx) {
    if (state.characters.length < 2) return;
    
    const { scale } = state;
    
    targetCtx.strokeStyle = COLORS.centroidLine;
    targetCtx.lineWidth = 3;
    targetCtx.setLineDash([8, 4]);
    targetCtx.beginPath();
    
    state.characters.forEach((char, idx) => {
        const cx = (char.x + char.width / 2) * scale;
        const cy = (char.y + char.height / 2) * scale;
        
        if (idx === 0) {
            targetCtx.moveTo(cx, cy);
        } else {
            targetCtx.lineTo(cx, cy);
        }
    });
    
    targetCtx.stroke();
    targetCtx.setLineDash([]);
}

