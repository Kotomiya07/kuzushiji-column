function formatTimestamp(value) {
    if (!value) return '未学習';
    const date = new Date(value);
    if (Number.isNaN(date.getTime())) return String(value);
    return date.toLocaleString('ja-JP', {
        year: 'numeric',
        month: '2-digit',
        day: '2-digit',
        hour: '2-digit',
        minute: '2-digit',
    });
}

function formatMetrics(metrics) {
    if (!metrics || Object.keys(metrics).length === 0) return '未計算';
    const accuracy = Number(metrics.accuracy ?? 0).toFixed(2);
    const precision = Number(metrics.precision ?? 0).toFixed(2);
    const recall = Number(metrics.recall ?? 0).toFixed(2);
    const f1 = Number(metrics.f1 ?? 0).toFixed(2);
    return `Acc ${accuracy} / P ${precision} / R ${recall} / F1 ${f1}`;
}

function formatFeedbackSummary(summary) {
    if (!summary || Number(summary.event_count ?? 0) === 0) return 'まだ feedback なし';
    const accepted = Number(summary.accepted_count ?? 0);
    const corrected = Number(summary.corrected_count ?? 0);
    const rate = (Number(summary.acceptance_rate ?? 0) * 100).toFixed(1);
    const lastPageId = summary.last_page_id ? ` / 最終 ${summary.last_page_id}` : '';
    return `採用 ${accepted} / 修正 ${corrected} / 採用率 ${rate}%${lastPageId}`;
}

