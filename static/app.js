// ===== ページ移動 =====
async function navigatePage(direction, options = {}) {
    // ナビゲーション中なら無視（連続キー入力防止）
    if (state.isNavigating) return;
    
    const currentIndex = state.pages.indexOf(state.pageId);
    const newIndex = currentIndex + direction;
    
    if (newIndex < 0 || newIndex >= state.pages.length) return;
    
    state.isNavigating = true;
    
    try {
        let startedBackgroundSave = false;
        if (options.skipSave) {
            showToast('保存せず次のページへ移動しました');
        } else if (direction > 0 && isInitialAutoSaveCandidate()) {
            const snapshot = createSaveSnapshot();
            if (!startValidatedBackgroundSave(snapshot)) return;
            startedBackgroundSave = true;
        } else if (direction > 0 && state.initialAutoGenerated && !state.hasUserEdited && state.isDirty) {
            showToast('未分割が残っているため自動保存できません。保存またはスキップしてください。', true);
            return;
        }

        if (!options.skipSave && !startedBackgroundSave && state.isDirty) {
            const result = await showSaveDialog();
            if (result === 'cancel') return;
            if (result === 'save') {
                const snapshot = createSaveSnapshot();
                if (!startValidatedBackgroundSave(snapshot)) return;
                startedBackgroundSave = true;
            }
        }
        if (!options.skipSave && !startedBackgroundSave && state.isSegmentsDirty) {
            const result = await showSaveDialog();
            if (result === 'cancel') return;
            if (result === 'save') {
                const snapshot = createSaveSnapshot();
                if (!startValidatedBackgroundSave(snapshot)) return;
            }
        }
        
        const newPageId = state.pages[newIndex];
        elements.pageSelect.value = newPageId;
        await loadPageData(state.bookId, newPageId);
    } finally {
        state.isNavigating = false;
    }
}

function skipNextPage() {
    navigatePage(1, { skipSave: true });
}

