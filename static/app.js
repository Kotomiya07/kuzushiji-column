function buildColumnPredictionFeedbackFrom(sourceState) {
    const source = sourceState.predictionMeta?.columnSource;
    const boundaries = sourceState.predictionMeta?.columnBoundaries || [];
    if (!['column_boundary_model', 'global_column_boundary_model'].includes(source) || boundaries.length === 0) {
        return null;
    }

    const colIndexByCharId = new Map();
    sourceState.columns.forEach((column, colIdx) => {
        column.forEach((charIndex) => {
            const char = sourceState.characters[charIndex];
            if (char) colIndexByCharId.set(char.char_id, colIdx);
        });
    });

    let acceptedCount = 0;
    boundaries.forEach((boundary) => {
        const leftCol = colIndexByCharId.get(boundary.left_char_id);
        const rightCol = colIndexByCharId.get(boundary.right_char_id);
        const actualBreak = leftCol !== undefined && rightCol !== undefined && leftCol !== rightCol;
        if (Boolean(boundary.predicted_break) === actualBreak) {
            acceptedCount += 1;
        }
    });

    return {
        task_type: 'column',
        source,
        accepted_count: acceptedCount,
        corrected_count: boundaries.length - acceptedCount,
        total_count: boundaries.length,
        page_id: sourceState.pageId,
    };
}

