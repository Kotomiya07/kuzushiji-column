async function saveSegments() {
    const snapshot = createSaveSnapshot();
    const validationError = validateSegmentSaveSnapshot(snapshot);
    if (validationError) {
        showToast(validationError, true);
        return;
    }

    try {
        const result = await saveSnapshot(snapshot);
        showToast(result.segmentMessage || 'セグメントを保存しました');
    } catch (error) {
        console.error('セグメント保存に失敗:', error);
        showToast(error.message || 'セグメント保存に失敗しました', true);
    }
}

async function saveCurrentWork() {
    if (state.viewMode === 'segment') {
        if (state.isDirty) {
            await saveAnnotations();
            if (state.isDirty) return;
        }
        if (state.isSegmentsDirty) {
            await saveSegments();
            return;
        }
        if (!state.isDirty && !state.isSegmentsDirty) {
            showToast('保存する変更がありません');
        }
        return;
    }

    if (state.isDirty) {
        await saveAnnotations();
        return;
    }
    if (state.isSegmentsDirty) {
        await saveSegments();
        return;
    }
    showToast('保存する変更がありません');
}

function isInitialAutoSaveCandidate() {
    if (!state.initialAutoGenerated || state.hasUserEdited) return false;
    if (!state.isDirty || state.isSegmentsDirty) return false;
    if (state.hasOriginalAnnotations) return false;
    if (state.columns.length === 0) return false;
    return !hasUnassignedCharacters();
}

