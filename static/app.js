function cloneForSave(value) {
    return value == null ? value : JSON.parse(JSON.stringify(value));
}

function createSaveSnapshot() {
    return {
        bookId: state.bookId,
        pageId: state.pageId,
        characters: cloneForSave(state.characters) || [],
        columns: cloneForSave(state.columns) || [],
        columnIds: cloneForSave(state.columnIds) || [],
        segments: cloneForSave(state.segments) || [],
        segmentIds: cloneForSave(state.segmentIds) || [],
        isDirty: state.isDirty,
        isSegmentsDirty: state.isSegmentsDirty,
        hasOriginalAnnotations: state.hasOriginalAnnotations,
        hasCustomSegmentIds: state.hasCustomSegmentIds,
        predictionMeta: cloneForSave(state.predictionMeta),
    };
}

function renumberSnapshotSegmentIdsIfAuto(snapshot) {
    if (snapshot.hasCustomSegmentIds) return;
    if (!snapshot.segmentIds || snapshot.segmentIds.length === 0) return;

    snapshot.segmentIds = snapshot.segmentIds.map((_, idx) => `SEG${String(idx + 1).padStart(4, '0')}`);
}

function getUnassignedCount(snapshot) {
    const assigned = new Set(snapshot.columns.flat());
    return snapshot.characters.filter((_, i) => !assigned.has(i)).length;
}

function validateColumnSaveSnapshot(snapshot) {
    if (!snapshot.bookId || !snapshot.pageId) {
        return '保存対象のページがありません';
    }
    if (snapshot.columns.length === 0) {
        return '保存する列がありません';
    }

    const remaining = getUnassignedCount(snapshot);
    if (remaining > 0) {
        return `未分割の文字が${remaining}件あります。すべて分割してから保存してください。`;
    }

    return null;
}

function validateSegmentSaveSnapshot(snapshot) {
    if (snapshot.columns.length === 0 || snapshot.segments.length === 0) {
        return '保存するセグメントがありません';
    }
    if (snapshot.isDirty) {
        return '列の未保存変更があります。先に列を保存してください。';
    }
    if (!snapshot.hasOriginalAnnotations) {
        return 'セグメント保存には先に列の保存が必要です';
    }
    return null;
}

function buildSegmentsPayload(snapshot) {
    renumberSnapshotSegmentIdsIfAuto(snapshot);
    return snapshot.segments.map((segColIdxes, segIdx) => {
        const segId = snapshot.segmentIds[segIdx] || `SEG${String(segIdx + 1).padStart(4, '0')}`;
        const columnIds = segColIdxes.map((colIdx) => snapshot.columnIds[colIdx]).filter(Boolean);
        return { segment_id: segId, column_ids: columnIds };
    });
}

function isCurrentPageSnapshot(snapshot) {
    return state.bookId === snapshot.bookId && state.pageId === snapshot.pageId;
}

function applySnapshotSaveSuccess(snapshot, results) {
    if (!isCurrentPageSnapshot(snapshot)) return;

    if (results.savedColumns) {
        state.isDirty = false;
        state.initialAutoGenerated = false;
        state.hasOriginalAnnotations = true;
        state.columnIds = snapshot.columnIds.slice();
        state.columns.forEach((indices, colIdx) => {
            const colId = state.columnIds[colIdx];
            indices.forEach((charIdx) => {
                if (state.characters[charIdx]) state.characters[charIdx].column_id = colId;
            });
        });
    }

    if (results.savedSegments) {
        state.isSegmentsDirty = false;
        state.hasOriginalSegmentAnnotations = true;
        state.hasCustomSegmentIds = false;
        state.segmentIds = snapshot.segmentIds.slice();
        state.segments.forEach((segColIdxes, segIdx) => {
            const segId = state.segmentIds[segIdx];
            segColIdxes.forEach((colIdx) => {
                const indices = state.columns[colIdx];
                if (!indices) return;
                indices.forEach((charIdx) => {
                    if (state.characters[charIdx]) state.characters[charIdx].segment_id = segId;
                });
            });
        });
    }

    if (results.savedColumns || results.savedSegments) {
        state.hasUserEdited = false;
    }
    if (results.savedColumns) {
        restoreColumnsFromAnnotations();
    }
    if (results.savedColumns || results.savedSegments) {
        restoreSegmentsFromAnnotations();
    }
    updateButtons();
}

async function saveSnapshot(snapshot, options = {}) {
    const showGlobalLoading = options.showLoading !== false;
    const results = {
        columnMessage: null,
        segmentMessage: null,
        savedColumns: false,
        savedSegments: false,
    };

    if (!snapshot.isDirty && !snapshot.isSegmentsDirty) {
        return results;
    }

    if (showGlobalLoading) showLoading(true);

    try {
        if (snapshot.isDirty) {
            const validationError = validateColumnSaveSnapshot(snapshot);
            if (validationError) throw new Error(validationError);

            const predictionFeedback = buildColumnPredictionFeedbackFrom({
                predictionMeta: snapshot.predictionMeta,
                columns: snapshot.columns,
                characters: snapshot.characters,
                pageId: snapshot.pageId,
            });
            const columnsData = snapshot.columns.map(indices => ({
                char_ids: indices.map(i => snapshot.characters[i]?.char_id).filter(Boolean),
            }));

            const res = await fetch(`/api/books/${snapshot.bookId}/pages/${snapshot.pageId}/save`, {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify({
                    book_id: snapshot.bookId,
                    page_id: snapshot.pageId,
                    columns: columnsData,
                    prediction_feedback: predictionFeedback,
                }),
            });

            const result = await res.json();
            if (!res.ok || !result.success) {
                throw new Error(result.detail || '保存に失敗しました');
            }

            results.columnMessage = result.message;
            results.savedColumns = true;
            snapshot.isDirty = false;
            snapshot.hasOriginalAnnotations = true;
            snapshot.columnIds = snapshot.columns.map((_, i) => `COL${String(i + 1).padStart(4, '0')}`);
        }

        if (snapshot.isSegmentsDirty) {
            const validationError = validateSegmentSaveSnapshot(snapshot);
            if (validationError) throw new Error(validationError);

            const segmentsPayload = buildSegmentsPayload(snapshot);
            const predictionFeedback = buildSegmentPredictionFeedbackFrom({
                predictionMeta: snapshot.predictionMeta,
                segments: snapshot.segments,
                columnIds: snapshot.columnIds,
                pageId: snapshot.pageId,
            });

            const res = await fetch(`/api/books/${snapshot.bookId}/pages/${snapshot.pageId}/save_segments`, {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify({
                    book_id: snapshot.bookId,
                    page_id: snapshot.pageId,
                    segments: segmentsPayload,
                    prediction_feedback: predictionFeedback,
                }),
            });

            const result = await res.json();
            if (!res.ok || !result.success) {
                throw new Error(result.detail || 'セグメント保存に失敗しました');
            }

            results.segmentMessage = result.message;
            results.savedSegments = true;
            snapshot.isSegmentsDirty = false;
        }

        if (options.applyToCurrent !== false) {
            applySnapshotSaveSuccess(snapshot, results);
        }
        markPageAnnotated(snapshot.pageId);
        updateBookProgress();
        if (snapshot.bookId === state.bookId) {
            scheduleModelStatusRefresh(snapshot.bookId);
        }
        return results;
    } finally {
        if (showGlobalLoading) showLoading(false);
    }
}

function startBackgroundSave(snapshot) {
    if (!snapshot.isDirty && !snapshot.isSegmentsDirty) return;

    showToast(`保存中: ${snapshot.pageId}`);
    saveSnapshot(snapshot, { showLoading: false, applyToCurrent: false })
        .then((result) => {
            if (result.savedColumns || result.savedSegments) {
                showToast(`保存完了: ${snapshot.pageId}`);
            }
        })
        .catch((error) => {
            console.error('バックグラウンド保存に失敗:', error);
            showToast(`${snapshot.pageId} の保存に失敗しました: ${error.message}`, true);
        });
}

